macro_rules! inst {
    ($l:ident) => {
        struct $l {}
    };
}

macro_rules! braces_unwrap {
    ( $Callback : path, { $( $Src : tt )* } ) => {
        $Callback!
        (
            $( $Src )*
        );
    };
    ( $Callback : path, $( $Src : tt )* ) => {
        $Callback! ( $( $Src )*);
    };
}

macro_rules! for_each {
    ( $( $Each : tt ),* $(,)?) => {
        mod a {
            $( braces_unwrap!( inst, $Each );)*
        }
        mod b {
            $( braces_unwrap!( inst, $Each );)*
        }
    };
}

